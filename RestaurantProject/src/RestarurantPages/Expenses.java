    private void tbl_expMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbl_expMouseClicked
        // TODO add your handling code here:       
        try{
            //display the values by selecting a row 
            int t = tbl_exp.getSelectedRow();
            TableModel model = tbl_exp.getModel();
            txt_itemID.setText(model.getValueAt(t, 0).toString());
            txt_ItemName.setText(model.getValueAt(t, 1).toString());
            txt_Price.setText(model.getValueAt(t, 2).toString());
            s_quantity.setValue(Integer.parseInt((String)model.getValueAt(t, 3)));
            txt_ExpTotal.setText(model.getValueAt(t, 4).toString());
        }//end of try
        catch(Exception e){
            JOptionPane.showMessageDialog(this, e.getMessage());   
        }//end of catch
    }//GEN-LAST:event_tbl_expMouseClicked

