    private void btn_saveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_saveActionPerformed
        // TODO add your handling code here: 
          int x = JOptionPane.showConfirmDialog(null, "Are you sure you want to save this expenses?","Expenses saved ",JOptionPane.YES_NO_OPTION,JOptionPane.QUESTION_MESSAGE);
            if(x==0){
        try{
         //all displayed  values will be saved  in to the databes  
        String sql = "INSERT INTO `expenses`(`Item Name`, `Price`, `Quantity`, `Exp.Total`) VALUES (?,?,?,?)";
        pst=con.prepareStatement(sql);
        pst.setString(1, txt_ItemName.getText());
        pst.setString(2, txt_Price.getText());
        pst.setString(3, s_quantity.getValue().toString());
        pst.setString(4, txt_ExpTotal.getText());
        pst.execute();
           JOptionPane.showMessageDialog(null,"Expenses Added");      
        }//end of try
        catch(Exception e){
            JOptionPane.showMessageDialog(null, e.getMessage());
        }//end of catch 
            }
    }//GEN-LAST:event_btn_saveActionPerformed

