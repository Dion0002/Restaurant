    private void btn_UpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_UpdateActionPerformed
        // TODO add your handling code here:
         try{
            //get table model 
            DefaultTableModel model = (DefaultTableModel)tbl_exp.getModel();
            if(tbl_exp.getSelectedRowCount()==1){
            
           String ID = txt_itemID.getText();
           String name = txt_ItemName.getText();
           String price = txt_Price.getText();
           String quantity = s_quantity.getValue().toString();
           String Total = txt_ExpTotal.getText();
            
           
           //update value on table display
           model.setValueAt(ID, tbl_exp.getSelectedRow(),0);
           model.setValueAt(name, tbl_exp.getSelectedRow(),1);
           model.setValueAt(price, tbl_exp.getSelectedRow(),2);
           model.setValueAt(quantity, tbl_exp.getSelectedRow(),3);
           model.setValueAt(Total, tbl_exp.getSelectedRow(),4);
                
            }
          
          
            }//end of try
        catch(Exception e){ 
            JOptionPane.showMessageDialog(null, e.getMessage());
         }//end of catch
        
    }//GEN-LAST:event_btn_UpdateActionPerformed

