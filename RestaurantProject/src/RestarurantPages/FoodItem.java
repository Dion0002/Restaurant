    private void btn_searchItemIDActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_searchItemIDActionPerformed
        // TODO add your handling code here:
            try{
                // displaying all thge values from databse to the filds where id is typed in the search field
                String s = "SELECT * FROM `add_fooditem` WHERE `Item ID`=? ";
                pst = con.prepareStatement(s);
                pst.setString(1,txt_searchItemID.getText());
                rs=pst.executeQuery();
            if(rs.next()){   
                String id =rs.getString("Item ID");
                txt_itemID.setText(id);

                String FC =rs.getString("Item Name");
                txt_ItemName.setText(FC);

                String FD =rs.getString("Price");
                txt_Price.setText(FD);
                
                String Category = rs.getString("Category");
                cmb_category.setSelectedItem(Category);  
            }//end of if 
        
        }//end of try
        catch(Exception e){   
        }//end of catch    
    }//GEN-LAST:event_btn_searchItemIDActionPerformed

