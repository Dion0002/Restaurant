    private void btn_addActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_addActionPerformed
        // TODO add your handling code here:
        
         //ask the user is he/she want to add new item 
        int x = JOptionPane.showConfirmDialog(null, "Are you sure you want to add this Item?","Add Item",JOptionPane.YES_NO_OPTION,JOptionPane.QUESTION_MESSAGE);
        if(x==0){
        try{
            //add the new item into the database
            String I ="INSERT INTO `add_fooditem`(`Item Name`, `Price`, `Category`) VALUES (?,?,?)"; 
            pst=con.prepareStatement(I);
            pst.setString(1, txt_ItemName.getText());
            pst.setString(2, txt_Price.getText());
            pst.setString(3, cmb_category.getSelectedItem().toString());
            pst.execute();
            JOptionPane.showMessageDialog(null, "Item Added");
        }//end of try
        catch(Exception e){       
        }//end of catch
   }//end of if 
   
 FoodItem_table();//calling the method
        
    }//GEN-LAST:event_btn_addActionPerformed

